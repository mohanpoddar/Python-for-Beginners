#!/usr/bin/python

# print() is a function which is used to display the specified content on the screen.

print()    #Print a blank line
print("Learn use of Syntax of print() function: sep and end")

print()    #Print a blank line
print(5,10,15)                                  # Space is the default separator

print()    #Print a blank line
print(25,35,45,55,sep="*")			# * as separator

print()    #Print a blank line
print(2,4,6,8,10,sep="_*_")			# _*_ as separator

print()    #Print a blank line
print(3,6,9,12,15,18,sep='\n')			# '\n' (newline character) as separator

print()    #Print a blank line
print('a','e','i','o','u',sep='\t')		# '\t' (tab) as separator

print()    #Print a blank line
print('python','ansible','panda','redhat',sep=',',end='!!!')		# Use of both 'sep' and 'end'
print('\n')
print()    #Print a blank line
