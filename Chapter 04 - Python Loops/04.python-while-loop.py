# Understanding Python loops - while

#:-------------------------------------------- Prog. - 01 --------------------------------------------:
print("Running programme : Prog. - 01")
print("Display number till 10:")
time.sleep(1)

Num = 1
while Num <= 10:
    print(Num)
    Num += 1
print()

#:-------------------------------------------- Prog. - 02 --------------------------------------------:
print("Running programme : Prog. - 02")
print("Display number till 10:")
time.sleep(1)

Num = 1
while Num <= 10:
    print(Num, end = " ")
    Num += 1
